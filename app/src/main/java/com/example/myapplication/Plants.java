package com.example.myapplication;

public class Plants {

    private String description;
    private String disease_cause;
    private String disease_name;
    private String treatment;

    // Getters and setters
    public String getDescription() { return description; }
    public void setDescription(String description) { this.description = description; }
    public String getDiseaseCause() { return disease_cause; }
    public void setDiseaseCause(String disease_cause) { this.disease_cause = disease_cause; }
    public String getDiseaseName() { return disease_name; }
    public void setDiseaseName(String disease_name) { this.disease_name = disease_name; }
    public String getTreatment() { return treatment; }
    public void setTreatment(String treatment) { this.treatment=treatment;}
}
